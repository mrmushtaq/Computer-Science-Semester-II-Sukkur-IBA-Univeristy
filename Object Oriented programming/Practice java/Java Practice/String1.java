public class String1 {
    public static void main(String[] args) {

        System.out.println(89>>3); // 11
        System.out.println(-89>>3);// -12
        System.out.println(-89>>>3);//big number
        System.out.println(89>>>3);//11

        System.out.println(10<<3); // 80
        System.out.println(-10<<3);// -80

        int x=-3453536;
        System.out.println(~x);

    }
}